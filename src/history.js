import {
  HISTORY_COMPACT_AFTER_CHARS,
  HISTORY_COMPACT_KEEP_MESSAGES
} from "./config.js";

const SUMMARY_MARKER = "Conversation summary generated by Mini Claude Code.";

export function shouldCompactHistory(messages) {
  if (HISTORY_COMPACT_AFTER_CHARS <= 0) return false;
  if (messages.length <= HISTORY_COMPACT_KEEP_MESSAGES + 2) return false;
  return estimateHistoryChars(messages) > HISTORY_COMPACT_AFTER_CHARS;
}

export async function compactHistory({ messages, callModel, recordTokenUsage }) {
  const splitIndex = findCompactionSplit(messages);
  if (splitIndex <= 0) {
    return { compacted: false, reason: "no safe split point" };
  }

  const older = messages.slice(0, splitIndex);
  const recent = messages.slice(splitIndex);
  const beforeChars = estimateHistoryChars(messages);
  const response = await callModel([
    {
      role: "user",
      content: [
        "Summarize the following Mini Claude Code conversation history for future context.",
        "Keep durable user intent, repository facts, files changed, commands run, tool results, unresolved tasks, decisions, and constraints.",
        "Omit repetitive logs and transient wording. Preserve exact file paths, env var names, command names, and errors when important.",
        "",
        serializeMessagesForSummary(older)
      ].join("\n")
    }
  ], {
    system: "You write compact but useful coding-agent conversation summaries.",
    tools: [],
    stream: false
  });

  const usageDetail = recordTokenUsage(response.usage);
  const summary = response.content
    .filter((block) => block.type === "text")
    .map((block) => block.text)
    .join("\n")
    .trim();

  if (!summary) {
    return { compacted: false, reason: "empty summary", usageDetail };
  }

  messages.splice(0, messages.length, {
    role: "user",
    content: [
      SUMMARY_MARKER,
      "This is hidden continuity context, not a new user request.",
      "",
      summary
    ].join("\n")
  }, ...recent);

  return {
    compacted: true,
    beforeChars,
    afterChars: estimateHistoryChars(messages),
    removedMessages: older.length,
    keptMessages: recent.length,
    usageDetail
  };
}

export function estimateHistoryChars(messages) {
  return messages.reduce((sum, message) => sum + JSON.stringify(message).length, 0);
}

function findCompactionSplit(messages) {
  const keep = Math.max(Number(HISTORY_COMPACT_KEEP_MESSAGES) || 12, 4);
  let splitIndex = Math.max(messages.length - keep, 1);

  while (splitIndex < messages.length && isToolResultMessage(messages[splitIndex])) {
    splitIndex += 1;
  }

  if (messages.length - splitIndex < 2) return 0;
  return splitIndex;
}

function isToolResultMessage(message) {
  return message.role === "user"
    && Array.isArray(message.content)
    && message.content.some((block) => block.type === "tool_result");
}

function serializeMessagesForSummary(messages) {
  return messages.map((message, index) => {
    const content = typeof message.content === "string"
      ? message.content
      : JSON.stringify(message.content);
    return [
      `<message index="${index}" role="${message.role}">`,
      content,
      "</message>"
    ].join("\n");
  }).join("\n\n");
}
